package lesson2_4logicalOperators;

public class Main {

    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;

        System.out.println("a && b: " + (a && b));
        System.out.println("a || b: " + (a || b));
        System.out.println("a ^ b: " + (a ^ b));
        System.out.println("!a: " + (!a));
    }
}
