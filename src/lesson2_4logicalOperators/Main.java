package lesson2_4logicalOperators;

public class Main {

    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;

        // a and b
        System.out.println("a && b: " + (a && b));
        // a or b
        System.out.println("a || b: " + (a || b));
        // a XOR b
        System.out.println("a ^ b: " + (a ^ b));
        // not a
        System.out.println("!a: " + (!a));
    }
}
