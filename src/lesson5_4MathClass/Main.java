package lesson5_4MathClass;

public class Main {

    public static void main(String[] args) {
        System.out.println(Math.ceil(25.3)); // round up
        System.out.println(Math.floor(8.9)); // round down
        System.out.println(Math.abs(-13.3)); // absolute value
        System.out.println(Math.min(7.3, 3.3)); // minimum of both
        System.out.println(Math.max(7.3, 3.3)); // maximum of both
        System.out.println(Math.pow(3, 4)); // 3 power 4
        System.out.println(Math.sqrt(25)); // square root
        System.out.println(Math.sin(90)); // Sinus
    }
}
